using UnityEngine;

/// <summary>
/// Класс для хранения информации о выборе юнитов игроком перед началом матча.
/// Обычно используется при предварительной настройке состава армии.
/// </summary>
public class PlayerUnitSelectionData
{
    /// <summary>
    /// Количество выбранных медленных юнитов игроком.
    /// Обычно такие юниты обладают большим радиусом атаки, но меньшей скоростью.
    /// </summary>
    public int SlowUnitCount;

    /// <summary>
    /// Количество выбранных быстрых юнитов игроком.
    /// Обычно такие юниты быстрее перемещаются, но имеют меньший радиус атаки.
    /// </summary>
    public int FastUnitCount;

    /// <summary>
    /// Готов ли игрок к началу игры.
    /// Условие готовности — выбор хотя бы одного юнита.
    /// </summary>
    public bool IsReady => SlowUnitCount + FastUnitCount > 0;
}
